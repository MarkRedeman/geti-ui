import fs from 'node:fs';
import path from 'node:path';

const REPO_ROOT = path.resolve(new URL('.', import.meta.url).pathname, '..', '..');
const DOCS_ROOT = path.join(REPO_ROOT, 'documentation', 'docs');
const HERO_STATS_OUTPUT_PATH = path.join(REPO_ROOT, 'documentation', 'theme', 'hero-stats.generated.ts');

function walkFiles(dir, predicate, out = []) {
    const entries = fs.readdirSync(dir, { withFileTypes: true });

    for (const entry of entries) {
        if (entry.name.startsWith('.')) continue;
        const fullPath = path.join(dir, entry.name);

        if (entry.isDirectory()) {
            walkFiles(fullPath, predicate, out);
            continue;
        }

        if (entry.isFile() && predicate(fullPath)) {
            out.push(fullPath);
        }
    }

    return out;
}

function countMdxFiles(dir, { excludeBaseNames = [] } = {}) {
    const exclude = new Set(excludeBaseNames);

    return walkFiles(
        dir,
        (filePath) => {
            if (!filePath.endsWith('.mdx')) return false;
            const baseName = path.basename(filePath);
            return !exclude.has(baseName);
        },
        []
    ).length;
}

function countLinesMatching(filePath, matcher) {
    const text = fs.readFileSync(filePath, 'utf-8');
    return text.split(/\r?\n/).filter((line) => matcher.test(line)).length;
}

function countRegexMatches(filePath, regex) {
    const text = fs.readFileSync(filePath, 'utf-8');
    return [...text.matchAll(regex)].length;
}

const counts = {
    components: countMdxFiles(path.join(DOCS_ROOT, 'components'), {
        excludeBaseNames: ['_template.mdx', 'installation.mdx', 'theming.mdx'],
    }),
    charts:
        countMdxFiles(path.join(DOCS_ROOT, 'charts', 'compositions')) +
        countMdxFiles(path.join(DOCS_ROOT, 'charts', 'primitives')),
    blocks: countMdxFiles(path.join(DOCS_ROOT, 'blocks'), {
        excludeBaseNames: ['installation.mdx'],
    }),
    smartTools: countMdxFiles(path.join(DOCS_ROOT, 'smart-tools'), {
        excludeBaseNames: ['installation.mdx'],
    }),
    assets: countLinesMatching(path.join(REPO_ROOT, 'packages', 'ui', 'src', 'assets', 'icons', 'index.ts'),
        /^export\s+\{/),
    mcpTools: countRegexMatches(path.join(REPO_ROOT, 'packages', 'mcp', 'src', 'server.ts'), /server\.registerTool\(/g),
};

const heroStats = [
    { value: String(counts.components), label: 'Components', href: '/components/ui/Button' },
    { value: String(counts.charts), label: 'Charts', href: '/charts/overview' },
    { value: String(counts.blocks), label: 'Blocks', href: '/blocks/installation' },
    { value: String(counts.smartTools), label: 'Smart Tools', href: '/smart-tools/installation' },
    { value: String(counts.assets), label: 'Assets', href: '/assets/icons' },
    { value: String(counts.mcpTools), label: 'MCP Tools', href: '/ai/overview' },
];

function toTsModule(stats) {
    const lines = [
        '/* AUTO-GENERATED FILE. DO NOT EDIT MANUALLY. */',
        '/* Generated by: documentation/scripts/count-home-hero-stats.mjs */',
        '',
        'export type HeroStat = { value: string; label: string; href: string };',
        '',
        'export const heroStats: HeroStat[] = ' + JSON.stringify(stats, null, 4) + ';',
        '',
    ];

    return lines.join('\n');
}

const shouldWrite = process.argv.includes('--write');

if (shouldWrite) {
    fs.writeFileSync(HERO_STATS_OUTPUT_PATH, toTsModule(heroStats), 'utf-8');
    console.log(`Wrote ${path.relative(REPO_ROOT, HERO_STATS_OUTPUT_PATH)}`);
}

console.log('Home hero stats (computed):');
console.table(heroStats.map(({ label, value }) => ({ label, value })));

console.log('\nPaste this into documentation/theme/HomeLayout.tsx:');
console.log('const heroStats = ' + JSON.stringify(heroStats, null, 4) + ';');
